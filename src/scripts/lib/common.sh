resolve_market_feed_python_bin() {
  local candidate resolved

  if market_feed_python_has_deps "$MARKET_FEED_PYTHON_BIN"; then
    printf '%s\n' "$MARKET_FEED_PYTHON_BIN"
    return 0
  fi

  for candidate in \
    "$AGENT_ROOT/.venv/bin/python" \
    "${CONDA_PREFIX:-}/bin/python" \
    python3 \
    python \
    /opt/miniconda3/bin/python3 \
    /opt/homebrew/bin/python3 \
    /usr/local/bin/python3 \
    /usr/bin/python3; do
    [[ -n "$candidate" ]] || continue
    if [[ "$candidate" == */* ]]; then
      [[ -x "$candidate" ]] || continue
      resolved="$candidate"
    else
      resolved="$(command -v "$candidate" 2>/dev/null || true)"
      [[ -n "$resolved" ]] || continue
    fi
    if market_feed_python_has_deps "$resolved"; then
      printf '%s\n' "$resolved"
      return 0
    fi
  done

  printf '%s\n' "$MARKET_FEED_PYTHON_BIN"
  return 1
}

acquire_lock() {
  local name="$1"
  local lock_dir="$AGENT_ROOT/runtime/state/${name}.lock"
  if ! mkdir "$lock_dir" 2>/dev/null; then
    log_line "$name lock exists, skipping run."
    exit 0
  fi
  trap "rmdir '$lock_dir' 2>/dev/null || true" EXIT
}


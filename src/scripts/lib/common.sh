codex_model_for_run_kind() {
  # Pick the Codex model for a run kind. Keep this in sync with
  # src/trading_agent/prompts/codex.py (SIMPLE_RUN_KINDS / resolve_codex_model).
  local run_kind="$1"
  if [[ -n "${CODEX_MODEL_FORCE:-}" ]]; then
    printf '%s' "$CODEX_MODEL_FORCE"
    return
  fi
  case "$run_kind" in
    account_snapshot|market_calendar|quote_snapshot_core|catalyst_enrichment|technical_research|intraday_snapshot|intraday_execute|email_notification*)
      printf '%s' "$CODEX_MODEL_MINI"
      ;;
    *)
      printf '%s' "$CODEX_MODEL"
      ;;
  esac
}

run_codex_prompt() {
  local run_kind="$1"
  local prompt_file="$2"
  local model
  model="$(codex_model_for_run_kind "$run_kind")"

  if [[ ! -f "$prompt_file" ]]; then
    log_line "$run_kind prompt file missing: $prompt_file"
    printf '%s missing prompt file: %s\n' "$(pt_now)" "$prompt_file" >> "$ERROR_LOG"
    return 1
  fi

  log_line "$run_kind starting mode=$TRADING_MODE model=$model kill_switch=$(kill_switch_status)"
  printf '{"timestamp":"%s","date":"%s","run_kind":"%s","status":"started","message":"prompt started"}\n' \
    "$(pt_now)" "$RUN_DATE_PT" "$run_kind" >> "$PROGRESS_LOG_DIR/${run_kind}.jsonl"

  if [[ "${CODEX_EXEC_DRY_RUN:-0}" == "1" ]]; then
    log_line "$run_kind CODEX_EXEC_DRY_RUN=1, not invoking codex exec."
    printf '{"timestamp":"%s","date":"%s","run_kind":"%s","status":"skipped","message":"CODEX_EXEC_DRY_RUN=1"}\n' \
      "$(pt_now)" "$RUN_DATE_PT" "$run_kind" >> "$PROGRESS_LOG_DIR/${run_kind}.jsonl"
    printf '%s DRY_RUN %s mode=%s kill_switch=%s prompt=%s\n' \
      "$(pt_now)" "$run_kind" "$TRADING_MODE" "$(kill_switch_status)" "$prompt_file" >> "$RUN_LOG"
    return 0
  fi

  if ! command -v "$CODEX_BIN" >/dev/null 2>&1; then
    log_line "$run_kind failed: CODEX_BIN not found: $CODEX_BIN"
    printf '%s CODEX_BIN not found: %s\n' "$(pt_now)" "$CODEX_BIN" >> "$ERROR_LOG"
    return 127
  fi

  local prompt_input
  prompt_input="$(mktemp "${TMPDIR:-/tmp}/robinhood-codex-${run_kind}.XXXXXX")"
  {
    build_runtime_block "$run_kind"
    cat "$prompt_file"
  } > "$prompt_input"

  set +e
  "$CODEX_BIN" --ask-for-approval never exec \
    --cd "$AGENT_ROOT" \
    --skip-git-repo-check \
    --sandbox workspace-write \
    -m "$model" \
    - < "$prompt_input" >> "$RUN_LOG" 2>> "$ERROR_LOG" &
  local codex_pid=$!
  (
    sleep "$CODEX_EXEC_TIMEOUT_SEC"
    if kill -0 "$codex_pid" 2>/dev/null; then
      printf '%s %s codex exec timeout after %ss\n' "$(pt_now)" "$run_kind" "$CODEX_EXEC_TIMEOUT_SEC" >> "$ERROR_LOG"
      kill -TERM "$codex_pid" 2>/dev/null || true
      sleep 5
      kill -KILL "$codex_pid" 2>/dev/null || true
    fi
  ) &
  local watchdog_pid=$!

  wait "$codex_pid"
  local status=$?
  kill "$watchdog_pid" 2>/dev/null || true
  wait "$watchdog_pid" 2>/dev/null || true
  rm -f "$prompt_input"
  set -e

  if [[ "$status" -ne 0 ]]; then
    log_line "$run_kind failed status=$status"
    printf '{"timestamp":"%s","date":"%s","run_kind":"%s","status":"failed","message":"codex exited with status %s"}\n' \
      "$(pt_now)" "$RUN_DATE_PT" "$run_kind" "$status" >> "$PROGRESS_LOG_DIR/${run_kind}.jsonl"
    return "$status"
  fi

  log_line "$run_kind completed status=0"
  printf '{"timestamp":"%s","date":"%s","run_kind":"%s","status":"completed","message":"codex completed"}\n' \
    "$(pt_now)" "$RUN_DATE_PT" "$run_kind" >> "$PROGRESS_LOG_DIR/${run_kind}.jsonl"
}
